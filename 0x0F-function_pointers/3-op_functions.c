#include "3-cal.h"

/**
 * op_add - calculate Sum
 * @a: first integer
 * @b: second integer
 *
 * Return: Sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculate Sum
 * @a: first integer
 * @b: second integer
 *
 * Return: differenece
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculate Sum
 * @a: first integer
 * @b: second integer
 *
 * Return: differenece
 *
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculates the divition
 * @a: first integer
 * @b: second integer
 *
 * Return: A division of 2 numbers
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates the divition
 * @a: first integer
 * @b: second integer
 *
 * Return: The remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
